    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $inventario = new Inventario();
        $inventario->Nombre_articulo = $request->get('Nombre_articulo');
        $inventario->Stock = $request->get('Stock');
        $inventario->CodigoBarra = $request->get('CodigoBarra');
        $inventario->id_sucursal = $request->get('id_sucursal');
        $inventario->id_categoria = $request->get('id_categoria');
        $inventario->save();

        return response('Articulo agregado al inventario',201);

    }

